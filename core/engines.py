# ENGINE 2: MOMENTUM BREAKOUT
# ══════════════════════════════════════════════════════════════════════════════


class MomentumBreakoutEngine(BaseEngine):
    """
    Momentum Breakout Entry Engine.

    Identifies tight consolidation (max 5% price spread over 20 days),
    then triggers a buy on a daily close above the 20-day high with
    >150% average volume.

    Conditions:
    1. 20-day price range (High-Low spread) must be <= 5% of price
    2. Today's close must be above the 20-day high
    3. Today's volume must be > 150% of 20-day average volume

    Priority: 2
    Allowed regimes: BULL only
    """

    name = "momentum_breakout"
    priority = 2

    def scan(
        self,
        df: pd.DataFrame,
        ticker: str,
        regime: RegimeSnapshot,
    ) -> EntrySignal | None:
        # Regime gate
        if not regime.allows_engine(self.name):
            return None

        if len(df) < BREAKOUT_CONSOLIDATION_DAYS + 5:
            return None

        last = df.iloc[-1]
        last_close = float(last["Close"])

        # Get the 20-day window BEFORE today
        lookback = df.iloc[-(BREAKOUT_CONSOLIDATION_DAYS + 1):-1]

        if len(lookback) < BREAKOUT_CONSOLIDATION_DAYS:
            return None

        # 1. Tight consolidation: max spread <= 5% of price
        highest = lookback["High"].max()
        lowest = lookback["Low"].min()
        spread_pct = ((highest - lowest) / last_close) * 100.0

        if spread_pct > BREAKOUT_MAX_SPREAD_PCT:
            return None

        # 2. Close above 20-day high
        high_20d = float(lookback["High"].max())
        if last_close <= high_20d:
            return None

        # 3. Volume > 150% average
        vol_ratio_series = volume_ratio(df, period=20)
        last_vol_ratio = float(vol_ratio_series.iloc[-1]) if not pd.isna(vol_ratio_series.iloc[-1]) else 1.0

        if last_vol_ratio < BREAKOUT_VOLUME_THRESHOLD:
            return None

        # Signal confirmed
        atr_series = atr(df, period=ATR_PERIOD)
        last_atr = float(atr_series.iloc[-1]) if not pd.isna(atr_series.iloc[-1]) else 0.0

        score = self.priority * last_vol_ratio

        return EntrySignal(
            engine=self.name,
            ticker=ticker,
            price=round(last_close, 2),
            score=round(score, 2),
            priority=self.priority,
            details={
                "consolidation_days": BREAKOUT_CONSOLIDATION_DAYS,
                "spread_pct": round(spread_pct, 2),
                "high_20d": round(high_20d, 2),
                "breakout_pct": round(((last_close - high_20d) / high_20d) * 100, 2),
                "volume_ratio": round(last_vol_ratio, 2),
                "atr": round(last_atr, 2),
            },
        )


