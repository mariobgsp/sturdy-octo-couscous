"""
Entry signal engines for the IHSG swing trading system.

Three independent engines, each with a consistent interface:
  engine.scan(df, ticker, regime) -> EntrySignal | None

Engine priority (highest to lowest):
  1. FVG Pullback        (priority 3) — pullback into Fair Value Gap
  2. Momentum Breakout   (priority 2) — breakout from tight range
  3. Buying on Weakness   (priority 1) — capitulation reversal
"""

