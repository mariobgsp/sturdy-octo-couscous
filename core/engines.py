# ─── Data Structures ─────────────────────────────────────────────────────────


@dataclass
class EntrySignal:
    """A concrete buy signal generated by an engine."""

    engine: str  # engine name identifier
    ticker: str
    price: float
    score: float  # ranking score (priority * volume_ratio)
    priority: int  # engine priority (3=highest, 1=lowest)
    details: dict = field(default_factory=dict)

    def __str__(self) -> str:
        return (
            f"[{self.engine}] {self.ticker} @ {self.price:,.0f} "
            f"score={self.score:.2f}"
        )


# ─── Base Engine ─────────────────────────────────────────────────────────────


class BaseEngine(ABC):
    """Abstract base for all entry engines."""

    name: str = "base"
    priority: int = 0

    @abstractmethod
    def scan(
        self,
        df: pd.DataFrame,
        ticker: str,
        regime: RegimeSnapshot,
    ) -> EntrySignal | None:
        """
        Evaluate a single stock for entry signals.

        Parameters
        ----------
        df : pd.DataFrame
            OHLCV data with DatetimeIndex.
        ticker : str
            Ticker code.
        regime : RegimeSnapshot
            Current market regime state.

        Returns
        -------
        EntrySignal | None
            Signal if triggered, else None.
        """
        ...


